    private void jbGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbGuardarActionPerformed
        int opcion = JOptionPane.showConfirmDialog(this, "Estas seguro que deseas actualizar esta orden de compra?", "Confirmación", JOptionPane.YES_NO_OPTION);
        if(opcion == JOptionPane.YES_OPTION){
            Ordencompra ordenCompra = new Ordencompra();
            ordenCompra.setIdordenCompra(this.orden.getIdordenCompra());
            ordenCompra.setFecha(this.jdcFecha.getDate());
            ordenCompra.setBlock((int)this.jsBlock.getValue());
            ordenCompra.setCultivo(this.cultivos.get(this.jcbCultivo.getSelectedIndex()));
            ordenCompra.setFolio(this.orden.getIdordenCompra());
            ordenCompra.setProveedor(this.proveedor);
            ordenCompra.setEstado("En proceso");
            double total = 0;
            for(Productos p : this.detalles){
                total += p.getTotal();
            }
            ordenCompra.setMontoTotal(total);
            OrdencompraJpaController daoOrden = new OrdencompraJpaController();
           
            try {
                daoOrden.edit(ordenCompra);
            } catch (NonexistentEntityException ex) {
                ex.printStackTrace();
            } catch (Exception ex) {
                ex.printStackTrace();
            }
            
            
            ProductosJpaController daoProductos = new ProductosJpaController();
            for(Productos p : this.detalles){
                if(daoProductos.findProductos(p.getIdProductos()) == null){
                    p.setIdOrdenCompra(ordenCompra);
                    p.setOrdenCompra(ordenCompra.getIdordenCompra());
                    daoProductos.create(p);
                }
            }
            for(Productos p: this.detallesEliminados){
                if(daoProductos.findProductos(p.getIdProductos()) != null){
                    try {
                        daoProductos.destroy(p.getIdProductos());
                    } catch (NonexistentEntityException ex) {
                        System.out.println(ex.getMessage());
                    }
                }
            }
            this.dispose();
        }
        
        
    }//GEN-LAST:event_jbGuardarActionPerformed

