    private void cargarInformacion(){
        this.jlTitulo.setText("Orden de compra [" + this.orden.getIdordenCompra() + "]");
        this.jdcFecha.setDate(this.orden.getFecha());
        this.jtfProveedor.setText(this.orden.getProveedor().getNombre());
        this.jsBlock.setValue(this.orden.getBlock());
        this.jcbCultivo.setSelectedItem(this.orden.getCultivo().getNombre());
        
        
        this.generarTabla();
        this.detalles = this.daoDetalles.findProductosByOrden(this.orden);
        this.llenarTabla();
    }

    private void generarTabla(){
        modelo = new DefaultTableModel();
        modelo.addColumn("Nombre");
        modelo.addColumn("Cantidad");
        modelo.addColumn("Total");
        this.jtProductos.setModel(modelo);
    }
    
    private void llenarTabla(){
        this.generarTabla();
        String[] campos = new String[3];
        for(Productos p : detalles){
            campos[0] = p.getIdProducto().getNombre();
            campos[1] = Integer.toString(p.getCantidad());
            campos[2] = " ";
            this.modelo.addRow(campos);
        }
    }
